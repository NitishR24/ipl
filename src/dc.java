        String s = cidd.getText();
        if(s.isEmpty()==true)
        {
            q.setText("PLEASE ENTER THE COACH ID");
        }
        try{
            Connection con= DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","NitishR","dbms");
            Statement st= con.createStatement();
            ResultSet rs=st.executeQuery("select cname,oid,tid from coach where cid='"+s+"'");
            if(rs.next()){

                tt1.setText(rs.getString(1));
                tt2.setText(Integer.toString(rs.getInt(2)));
                tt3.setText(Integer.toString(rs.getInt(3)));
            }
            else{
                q.setText("PLEASE ENTER THE CORRECT COACH ID");
            }

        }catch(SQLIntegrityConstraintViolationException e)
        {
            q.setText("EITHER TEAM ID OR COACH ID DOES NOT EXISTS");
        }
        catch(Exception e)
        {
            System.err.println(e);
        }
