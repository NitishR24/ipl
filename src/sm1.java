        if(rcid.isSelected())
        {   String s=cid.getText();
            try{
                Connection con= DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","NitishR","dbms");
                Statement st= con.createStatement();
                ResultSet rs=st.executeQuery("select m.mid,m.mloc,m.mdate,t.tid from match m,play t where m.mid='"+s+"' and m.mid=t.mid");
                if(rs.next()){
                    DefaultTableModel model = (DefaultTableModel) t.getModel();
                    String r1= rs.getString(3);
                    String r=r1.substring(0,10);
                    String a,b,c,d;
                    a=rs.getString(1);
                    b=rs.getString(2);
                    c=rs.getString(4);
                    rs.next();
                    d=rs.getString(4);
                    model.insertRow(1,new Object[]{a,b,r,c,d});
                }
                else{
                    setlabel.setText("NO RECORDS FOUND");
                }
            }
            catch(Exception e)
            {
                System.err.println(e);
            }
        }
